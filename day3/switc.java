package prime;

public class switc {

	
	public static void main(String[] args) 
	{
		int n=12;
		switch(n)
		{
		case 5:
			System.out.println("5");
		case 12:
			System.out.println("12");
		case 13:
			System.out.println("13");
			//break;
		default:
				System.out.println("Default case");
				break;
		}

	}

}
